------------------------------------------------------------
-- COMO FAZER O BACKUP DO MEU BANCO DE DADOS (DUMP)
------------------------------------------------------------
1. Clicar com o botão direito do mouse no meu banco de dados
2. Ir em tarefas (tasks) 
3. Fazer backup
4. Salvar no local desejado.
 
o Backup pode servir para alterações e passar o bd para uma nova maquina, ou em um novo servidor.
--------------------------------------------------------------------
-- RESTAURAR BD SALVO
---------------------------------
1.clicar na pasta de banco de dados e ir em "Restaurar Banco de Dados"
2. Dispositivo > Depois buscar o local armazenado